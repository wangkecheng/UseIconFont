使用步骤
1.先下载 FontLab-Studio 网址  http://soft.macx.cn/soft2042.htm
  安装需要密码 www.macx.cn

2.然后就是按教程   http://www.jianshu.com/p/115ca5633eef

3.注意事项:  label.text = @"\U0000E802"; 这里的 \U0000 表示unicode 前一部分 然后 E802 这个编码通过 FontLab-Studio打开的字体文件中 需要用的字体图标编号

4.对于文件夹 字体图标文件:里面以.ttf 结尾的就是需要用的字体图标文件
